        D /= norm
        
        # Calculate real sigma_x
        # Variance = <x^2> - <x>^2
        prob_density = np.abs(D)**2
        mean_x = np.sum(x * prob_density) * dx
        var_x = np.sum((x - mean_x)**2 * prob_density) * dx
