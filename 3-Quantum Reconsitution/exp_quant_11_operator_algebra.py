    # This non-commutativity is generated BY Lysis, not added to it.
    
    if comm_magnitude > 1e-10:
        print("\n[SUCCESS] Non-Trivial Algebra Detected.")
        print("          [Diamond, Shadow] != 0.")
        print("          Relational Lysis inherently generates a Quantum Phase Space.")
    else:
        print("\n[FAILURE] System is Classical (Commuting).")
        
