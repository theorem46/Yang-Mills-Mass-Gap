    classical_state = np.random.randn(N, N)
    classical_state = classical_state + classical_state.T # Force Symmetric
    
    D_c = 0.5 * (classical_state + classical_state.T)
    S_c = 0.5 * (classical_state - classical_state.T) # Should be 0
    comm_c = np.linalg.norm(np.dot(D_c, S_c) - np.dot(S_c, D_c))
    
    print(f"    Classical Input Commutator: {comm_c:.6f}")
