    comm_magnitude = np.linalg.norm(commutator)
    
    print(f"[*] Analyzing Geometric Commutation [Diamond, Shadow]...")
    print(f"    Diamond Norm (Geometry): {np.linalg.norm(Diamond):.4f}")
    print(f"    Shadow Norm (Residual):  {np.linalg.norm(Shadow):.4f}")
    print(f"    Commutator Magnitude:    {comm_magnitude:.6f}")
    
