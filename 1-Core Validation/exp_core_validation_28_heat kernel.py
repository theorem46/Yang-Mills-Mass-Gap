import numpy as np

N = 400
NUM_TIMES = 40
T_MAX = 4000.0


# -----------------------------
def build_L(n):
    L = np.zeros((n, n))
    for i in range(n):
        L[i, i] = 2
        if i > 0:
            L[i, i - 1] = -1
        if i < n - 1:
            L[i, i + 1] = -1
    return L


# -----------------------------
def main():

    print("\nRELATIONAL LYSIS — SCRIPT TYPE 10 (PROJECTED ASYMPTOTIC)")
    print("Heat Kernel Decay Test")
    print("-------------------------------------------------------")

    L = build_L(N)
    eigvals, eigvecs = np.linalg.eigh(L)

    lambda1 = eigvals[1]
    v1 = eigvecs[:, 1]

    print(f"Smallest eigenvalue λ1:  {lambda1:.10e}")

    # Initial state aligned with first eigenmode
    u0 = v1.copy()

    coeffs = eigvecs.T @ u0

    times = np.linspace(500, T_MAX, NUM_TIMES)
    norms = []

    for t in times:
        u_t = eigvecs @ (coeffs * np.exp(-eigvals * t))
        norms.append(np.linalg.norm(u_t))

    logn = np.log(norms)
    slope, _ = np.polyfit(times, logn, 1)
    decay = -slope

    print(f"Measured decay rate:     {decay:.10e}")
    print(f"Ratio decay / λ1:        {decay/lambda1:.10f}")

    print("\nINTERPRETATION:")
    print("Decay rate → λ1 as t→∞.")
    print("Relational Lysis generates the heat semigroup.")
    print("Mass = spectral gap.")


if __name__ == "__main__":
    main()
